<h1>Store Your Files Online</h1>

<p>
Put all your files in the cloud to keep important data backed up, available from any internet connected computer, and share files with others and collaborate on them by adding comments and more.
</p>

<p>
Simply upload your private files to "My Files" on the left, or any files you would like to share with other users in "Shared Files" to get started. Once a file is uploaded, you can click on the name of the file to add comments and notify other users of the new file upload.
</p>

<p>
If you use a Microsoft Windows 7 or newer operating system, you can <a href="/public/client/setup.exe" target="_blank">download the desktop client</a> that will synchronize your files automatically so you do not even have to worry about uploading anything.
</p>
