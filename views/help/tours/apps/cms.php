<h1>CMS (Content Management System)</h1>

<p>
It has been said that "Content is King" these days when it comes to marketing and sales. Netric CMS is a very easy to use but powerful content management solution that can be used to:
</p>

<h3>Build Dynamic Websites &amp; Blogs</h3>

<p>
Netric partners create powerful dynamic websites that are managed through the netric CRM. If you are interested in building a beautiful website or web application that is both fully customized for your organization and manageable through netric, please contact <a href="http://www.aereus.com/services/webdesign" target="_blank">Aereus</a>.
</p>

<h3>Syndicate RSS Feeds</h3>

<p>
Use Content Feeds to build RSS feeds that can be used on websites/blogs, email newsletters and more to allow end-users to receive updates every time you add a new post. Get started by clicking on the "New Feed" link on the left after closing this dialog.
</p>

<h3>Syndicate Podcasts</h3>

<p>
Content Feeds can also be used to syndicate Podcasts when you upload an MP3 file to each post.
</p>
